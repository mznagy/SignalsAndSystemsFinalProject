fs=5;
Ts=1/fs;
t=linspace(-100000,100000,200000*fs);
x=(10^(-3)).*t;
m=(sin(x)./x).^2;
y= 2.* pi .*(10.^5).*t;
r=m.*cos(y);
R=Ts.*fftshift(fft(r));
n=length(r);
f=(-n/2:n/2-1)*(fs/n);
w=2*pi*f.*(10.^6);
subplot(3, 1, 2);
plot(w,abs(R));
title("magnitude of R(s)");
subplot(3, 1, 3);
plot(w,angle(R));
title("phase of R(s)");
subplot(3, 1, 1);
plot(t,r);
title("r(t)");